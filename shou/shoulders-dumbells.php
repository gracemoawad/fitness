                       <p><i id="number">2</i>Sitting on the end of a flat bench, start with your arms by your side grasping the dumbbells. Begin each rep with a lateral raise, and then keeping your arms straight, bring them around to the front at shoulder height and then raise them above your head. To complete the rep, reverse what you have just done. Lower your arms halfway down so they are in front of you at shoulder height, then out to the side, and then return to the bottom. That is ONE rep! Make sure you keep your arms straight and maintain an upright position throughout the exercise. Avoid leaning back to raise the weight above your head.</p>
