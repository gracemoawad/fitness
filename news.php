  <p>Even if you exercise everyday – whether that’s at home, at the gym, or taking your dog for walks – you might not be getting as much physical activity as you think you are. The World Health Organization (WHO) recommends adults get at least 150 to 300 minutes of moderate to vigorous aerobic activity per week.

But, even with these clear guidelines, research shows that many of us overestimate the amount of exercise we do. In fact, around 36 per cent of people overestimate their physical activity levels – and an even higher proportion (61 per cent) of individuals that are not reaching adequate activity levels are overestimating their physical activity levels. One review even found people overestimate their activity levels anywhere from 36 per cent to 173 per cent.

One of the reasons people tend to overestimate their physical activity levels is because they don’t understand what is meant by moderate and vigorous intensity exercise. Not only could this mean people aren’t getting enough exercise, it could also mean those who do regularly exercise aren’t getting as much benefit as they might think they are. Fortunately, we can easily assess exercise intensity by measuring our heart rate during exercise.


Moderate intensity exercise is considered to be any physical activity that requires a person to work at 40-59 per cent of their heart rate reserve (the difference between resting heart rate and maximum heart rate). Anything higher than 60 per cent of your heart rate reserve is considered vigorous exercise. But while that sounds simple, everyone’s heart rate can be different at the same exercise intensity – affected by age, gender and fitness levels.

To calculate the target heart rate you need to achieve for the different exercise intensities, you first need to know your resting heart rate as well as your estimated maximum heart rate. Your resting heart rate can be measured by counting how many times your heart beats per minute while at rest. For an average adult, a healthy resting heart rate can be anywhere between 60-100 beats per minute. Your estimated heart rate maximum can be calculated by subtracting your age from 220. For example, someone who is 45 will have a heart rate maximum of around 175 beats per minute.

Once you have your resting and maximum heart rate, calculate the range (by subtracting the resting heart rate from the maximum) and multiply the proportion of desired heart rate reserve (such as 45 per cent – 0.45 – if you want to exercise at moderate intensity). Adding this value to your resting heart rate will give you the number of beats per minute you should try to achieve while exercising at the desired intensity.

For example, I have a resting heart rate of 45 and my estimated maximum heart rate is 187. To exercise at moderate intensity (40-59 per cent of my heart rate reserve), my heart rate would need to reach between 102 and 123 beats per minute during exercise. Anything above this would be considered vigorous intensity exercise.

It’s worth remembering that the heart is also a muscle, just like your arms and legs. In order to keep it healthy and strong

But if that sounds too complicated, there’s an easier (albeit subjective) option, called the rate of perceived exertion scale. This scale asks people to rate from six to 20 how difficult they feel the exercise they’re doing is. Moderate intensity exercise is thought to be between 12-13 (somewhat hard) and vigorous intensity as 14 and above. Be mindful here though, as this scale is subjective and may not be as accurate. Someone who has just started exercising would find jogging at six miles per hour as vigorous intensity while a regular marathon runner would find it to be moderate intensity or lower.

Similarly, you can count the number of steps you take in a given time. A step rate of 100 steps per minute is recommended to achieve moderate intensity. More than 100 steps per minute is needed to achieve vigorous intensity.

Now that we know what moderate and vigorous intensity exercise is, let’s put it into context to see how much exercise we may actually be doing. Let’s say you go for a walk five days a week for 30 minutes. This would add up to 150 minutes of activity per week but are these exercise bouts intense enough? Be mindful of your pace, whether or not you stop during your walks, and how exerting it feels.</p>
